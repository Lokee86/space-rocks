#!/usr/bin/env python3

from __future__ import annotations

import argparse
import json
import re
from pathlib import Path
from typing import Any


GENERATED_HEADER = "Code generated by tools/scripts/generate_packets.py; DO NOT EDIT."


def main() -> None:
    repo_root = Path(__file__).resolve().parents[2]
    parser = argparse.ArgumentParser(description="Generate packet files from shared JSON.")
    parser.add_argument(
        "--source",
        default="shared/packets/packets.json",
        help="Path to the packets JSON file, relative to the repo root.",
    )
    args = parser.parse_args()

    schema = json.loads((repo_root / args.source).read_text(encoding="utf-8"))
    structs_by_id = {struct["id"]: struct for struct in schema["structs"]}
    builders_by_id = {builder["id"]: builder for builder in schema.get("builders", [])}

    for output in schema["outputs"]:
        language = output["language"]
        if language == "go":
            lines = render_go(output, structs_by_id, schema)
        elif language == "gdscript":
            lines = render_gdscript(output, schema, builders_by_id)
        else:
            raise ValueError(f"Unsupported packet output language: {language}")

        output_path = repo_root / output["path"]
        output_path.parent.mkdir(parents=True, exist_ok=True)
        output_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
        print(f"generated {output_path.relative_to(repo_root)}")


def render_go(
    output: dict[str, Any],
    structs_by_id: dict[str, Any],
    schema: dict[str, Any],
) -> list[str]:
    lines = [
        f"// {GENERATED_HEADER}",
        f"package {output['package']}",
    ]

    imports = output.get("imports", {})
    if imports:
        lines.append("")
        if len(imports) == 1:
            alias, path = next(iter(imports.items()))
            lines.append(f'import {alias} "{path}"')
        else:
            lines.append("import (")
            for alias, path in sorted(imports.items()):
                lines.append(f'\t{alias} "{path}"')
            lines.append(")")

    if output.get("packet_types", False):
        lines.append("")
        lines.extend(render_go_packet_type_constants(schema))

    for struct_id in output["structs"]:
        lines.append("")
        lines.extend(render_go_struct(structs_by_id[struct_id]))

    return lines


def render_go_packet_type_constants(schema: dict[str, Any]) -> list[str]:
    lines = ["const ("]
    constants = [
        (f"PacketType{snake_to_go_name(packet_type['id'])}", packet_type["value"])
        for packet_type in schema.get("packet_types", [])
    ]
    max_name_length = max(len(name) for name, _ in constants)
    for name, value in constants:
        lines.append(f'\t{name.ljust(max_name_length)} = "{value}"')
    lines.append(")")
    return lines


def render_go_struct(struct: dict[str, Any]) -> list[str]:
    lines = [f"type {struct['id']} struct {{"]
    fields = [render_go_field(field) for field in struct["fields"]]
    max_name_length = max(len(field[0]) for field in fields)
    max_type_length = max(len(field[1]) for field in fields)

    for name, go_type, tag in fields:
        lines.append(f"\t{name.ljust(max_name_length)} {go_type.ljust(max_type_length)} `{tag}`")

    lines.append("}")
    return lines


def render_go_field(field: dict[str, Any]) -> tuple[str, str, str]:
    name = field.get("go_name", snake_to_go_name(field["name"]))
    go_type = field.get("go_type", go_type_for_field(field))
    return name, go_type, f'json:"{field["json"]}"'


def go_type_for_field(field: dict[str, Any]) -> str:
    field_type = field["type"]
    if field_type == "string":
        return "string"
    if field_type == "float":
        return "float64"
    if field_type == "int":
        return "int"
    if field_type == "bool":
        return "bool"
    if field_type == "map":
        key_type = scalar_go_type(field["key_type"])
        value_type = field.get("go_value_type", field["value_type"])
        return f"map[{key_type}]{value_type}"
    if field_type == "array":
        item_type = field.get("go_item_type", field["item_type"])
        return f"[]{item_type}"

    return field_type


def scalar_go_type(value_type: str) -> str:
    if value_type == "string":
        return "string"
    if value_type == "float":
        return "float64"
    if value_type == "int":
        return "int"
    if value_type == "bool":
        return "bool"
    raise ValueError(f"Unsupported scalar Go type: {value_type}")


def render_gdscript(
    output: dict[str, Any],
    schema: dict[str, Any],
    builders_by_id: dict[str, Any],
) -> list[str]:
    lines = [
        f"# {GENERATED_HEADER}",
        f"extends {output.get('base', 'RefCounted')}",
        "",
    ]

    lines.extend(render_gdscript_packet_type_constants(schema))
    lines.append("")
    lines.extend(render_gdscript_field_constants(schema))

    for builder_id in output.get("builders", []):
        lines.append("")
        lines.extend(render_gdscript_builder(builders_by_id[builder_id]))

    return lines


def render_gdscript_packet_type_constants(schema: dict[str, Any]) -> list[str]:
    lines = []
    for packet_type in schema.get("packet_types", []):
        name = f"TYPE_{constant_name(packet_type['id'])}"
        lines.append(f'const {name} := "{packet_type["value"]}"')
    return lines


def render_gdscript_field_constants(schema: dict[str, Any]) -> list[str]:
    fields = sorted({
        field["json"]
        for struct in schema["structs"]
        for field in struct["fields"]
    })
    return [f'const FIELD_{constant_name(field)} := "{field}"' for field in fields]


def render_gdscript_builder(builder: dict[str, Any]) -> list[str]:
    args = ", ".join(builder["args"])
    lines = [f"static func {builder['id']}({args}) -> Dictionary:"]
    lines.extend(render_gdscript_assignments("packet", builder["body"], indent=1))
    lines.append("\treturn packet")
    return lines


def render_gdscript_assignments(variable_name: str, value: Any, indent: int) -> list[str]:
    prefix = "\t" * indent
    if isinstance(value, dict):
        lines = [f"{prefix}var {variable_name} := {{}}"]
        for key, item in value.items():
            key_expr = gdscript_field_constant(key)
            if isinstance(item, dict):
                child_name = f"{variable_name}_{key}"
                lines.extend(render_gdscript_assignments(child_name, item, indent))
                lines.append(f"{prefix}{variable_name}[{key_expr}] = {child_name}")
            else:
                lines.append(f"{prefix}{variable_name}[{key_expr}] = {gdscript_leaf(item)}")
        return lines

    raise ValueError("Top-level GDScript builder body must be a dictionary")


def gdscript_leaf(value: Any) -> str:
    if isinstance(value, str):
        if value.startswith("$"):
            return value[1:]
        type_name = {
            "input": "TYPE_INPUT",
            "client_config": "TYPE_CLIENT_CONFIG",
            "state": "TYPE_STATE",
            "bullet_blast": "TYPE_BULLET_BLAST",
        }.get(value)
        if type_name is not None:
            return type_name
        return json.dumps(value)
    if isinstance(value, bool):
        return "true" if value else "false"
    if value is None:
        return "null"
    return str(value)


def gdscript_field_constant(field: str) -> str:
    return f"FIELD_{constant_name(field)}"


def snake_to_go_name(value: str) -> str:
    parts = value.split("_")
    return "".join(go_word(part) for part in parts)


def go_word(value: str) -> str:
    if value.lower() == "id":
        return "ID"
    return value[:1].upper() + value[1:]


def constant_name(value: str) -> str:
    return re.sub(r"[^A-Za-z0-9]+", "_", value).upper()


if __name__ == "__main__":
    main()
