from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Any


GENERATED_HEADER = "Code generated by tools/scripts/generate_constants.py; DO NOT EDIT."
VECTOR_COMPONENTS = {"x": 0, "y": 1}


def repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def load_schema(schema_path: Path) -> dict[str, Any]:
    return json.loads(schema_path.read_text(encoding="utf-8"))


def default_output_path(input_path: Path, extension: str) -> Path:
    return input_path.with_suffix(extension)


def parse_gdscript_constants(path: Path) -> dict[str, Any]:
    constants: dict[str, Any] = {}
    pattern = re.compile(r"^const\s+(\w+)\s*:?=\s*(.+)$")

    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        match = pattern.match(line)
        if match is None:
            continue

        constants[match.group(1)] = parse_gdscript_value(match.group(2))

    return constants


def parse_go_constants(path: Path) -> dict[str, Any]:
    constants: dict[str, Any] = {}
    pattern = re.compile(r"^(\w+)\s*=\s*(.+)$")

    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        match = pattern.match(line)
        if match is None:
            continue

        constants[match.group(1)] = parse_number(match.group(2))

    return constants


def parse_gdscript_value(value: str) -> Any:
    vector_match = re.match(r"^Vector2\(([^,]+),\s*([^)]+)\)$", value)
    if vector_match is not None:
        return [parse_number(vector_match.group(1)), parse_number(vector_match.group(2))]

    return parse_number(value)


def parse_number(value: str) -> int | float:
    value = value.strip()
    if re.match(r"^-?\d+$", value):
        return int(value)
    return float(value)


def render_gdscript_from_go(schema: dict[str, Any], source_constants: dict[str, Any]) -> list[str]:
    output = output_for_language(schema, "gdscript")
    groups_by_id = groups_by_id_from_schema(schema)
    lines = [
        f"# {GENERATED_HEADER}",
        f"extends {output.get('base', 'RefCounted')}",
    ]

    for group_id in output["groups"]:
        group_lines: list[str] = []
        for constant in groups_by_id[group_id]["constants"]:
            gd_target = constant.get("targets", {}).get("gdscript")
            go_target = constant.get("targets", {}).get("go")
            if gd_target is None or go_target is None:
                continue

            value = value_from_source_target(source_constants, go_target, constant["type"])
            group_lines.append(f"const {gd_target['name']} := {format_gdscript_value(constant['type'], value)}")

        if group_lines:
            lines.append("")
            lines.extend(group_lines)

    return lines


def render_go_from_gdscript(schema: dict[str, Any], source_constants: dict[str, Any]) -> list[str]:
    output = output_for_language(schema, "go")
    groups_by_id = groups_by_id_from_schema(schema)
    lines = [
        f"// {GENERATED_HEADER}",
        f"package {output['package']}",
        "",
        "const (",
    ]

    first_group = True
    for group_id in output["groups"]:
        group_lines: list[str] = []
        for constant in groups_by_id[group_id]["constants"]:
            gd_target = constant.get("targets", {}).get("gdscript")
            go_target = constant.get("targets", {}).get("go")
            if gd_target is None or go_target is None:
                continue

            value = source_constants[gd_target["name"]]
            group_lines.extend(render_go_constant_lines(constant, go_target, value))

        if not group_lines:
            continue

        if not first_group:
            lines.append("")
        first_group = False

        lines.extend(f"\t{line}" for line in align_go_constants(group_lines))

    lines.append(")")
    return lines


def output_for_language(schema: dict[str, Any], language: str) -> dict[str, Any]:
    for output in schema["outputs"]:
        if output["language"] == language:
            return output

    raise ValueError(f"No {language} output found in constants schema")


def groups_by_id_from_schema(schema: dict[str, Any]) -> dict[str, Any]:
    return {group["id"]: group for group in schema["groups"]}


def value_from_source_target(source_constants: dict[str, Any], target: Any, value_type: str) -> Any:
    if isinstance(target, list):
        if value_type != "vector2":
            raise ValueError("Only vector2 constants can be reconstructed from multiple targets")

        value = [0.0, 0.0]
        for item in target:
            value[VECTOR_COMPONENTS[item["component"]]] = source_constants[item["name"]]
        return value

    return source_constants[target["name"]]


def render_go_constant_lines(constant: dict[str, Any], target: Any, value: Any) -> list[str]:
    if isinstance(target, list):
        lines: list[str] = []
        for item in target:
            component_value = value[VECTOR_COMPONENTS[item["component"]]]
            lines.append(f"{item['name']} = {format_number(component_value)}")
        return lines

    return [f"{target['name']} = {format_number(value)}"]


def format_gdscript_value(value_type: str, value: Any) -> str:
    if value_type == "vector2":
        return f"Vector2({format_number(value[0])}, {format_number(value[1])})"

    return format_number(value)


def format_number(value: int | float) -> str:
    if isinstance(value, int):
        return str(value)
    if value.is_integer():
        return f"{value:.1f}"
    return str(value)


def align_go_constants(lines: list[str]) -> list[str]:
    split_lines = [line.split(" = ", 1) for line in lines]
    max_name_length = max(len(parts[0]) for parts in split_lines)

    return [
        f"{name.ljust(max_name_length)} = {value}"
        for name, value in split_lines
    ]
